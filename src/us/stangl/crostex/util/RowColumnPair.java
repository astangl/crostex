package us.stangl.crostex.util;

/**
 * Pair holding an integer (row, column)
 * @author Alex Stangl
 */
public class RowColumnPair {
	public final int row;
	public final int column;
	
	public RowColumnPair(int row, int column) {
		this.row = row;
		this.column = column;
	}
}
